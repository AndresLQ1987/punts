package punts;

public class Punt {

	private int x;
	private int y;

	public Punt(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public Punt() {
		x = 0;
		y = 0;
	}

	public int getX() {
		return x;
	}

	public int getY() {

		return y;
	}

	public void setX(int x) {
		this.x = x;
	}

	public void setY(int y) {
		this.y = y;
	}

}
